import Foundation

/// Custom hex color names.
///
/// All the color names are picked by [ArtyClick Color](https://colors.artyclick.com/color-name-finder/).
public enum HexColor: UInt {
    /// Apricot color: `#F3B562`.
    case apricot = 0xF3B562
    /// Buttery white color: `#FFFDEE`.
    case butteryWhite = 0xFFFDEE
    /// Cadet color: `#59706A`.
    case cadet = 0x59706A
    /// Cornell Red color: `#AF1313`.
    case cornellRed = 0xAF1313
    /// Curious Blue color: `#2A9DCE`.
    case curiousBlue = 0x2A9DCE
    /// Heavy metal color: `#2C2C2C`.
    case heavyMetal = 0x2C2C2C
    /// Sherwood green color: `#044132`.
    case sherwoodGreen = 0x044132
    /// Summer green color: `#8CBEB2`.
    case summerGreen = 0x8CBEB2
    /// Vampire grey color: `#5C4B51`.
    case vampireGrey = 0x5C4B51
    /// White smoke color: `#F5F5F5`.
    case whiteSmoke = 0xF5F5F5
}
